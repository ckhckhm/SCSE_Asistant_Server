  /**
   * 生命周期函数--监听页面显示
   */
  onShow: function () {
    const that = this;
    wx.getStorage({
      key: 'status',
      success: function (res) {
        if (res.data == 1) {
          const userID = wx.getStorageSync('user').userID
          const userClass = wx.getStorageSync('info')
          that.setData({
            userID,
            userClass
          })
        }
        that.setData({
          status: res.data,
        })
      }
    })
  },

